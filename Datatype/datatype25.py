
# Python3 code to demonstrate 
# Check if dictionary is empty 
# using not operator 
  
# initializing empty dictionary 
test_dict = [{},{},{}]
  
# printing original dictionary 
print("The original dictionary : " + str(list(test_dict)))
  
# using not operator 
# Check if dictionary is empty  
res = not test_dict 
  
# print result 
print("Is dictionary empty ? : " + str(res)) 
